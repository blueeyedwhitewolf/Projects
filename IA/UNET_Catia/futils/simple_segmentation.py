def segment_lung_from_ct_scan(ct_scan):
    return np.asarray(Parallel(n_jobs=8)(delayed(get_segmented_lungs)(i) for i in ct_scan))


def segment(scan,intercept=-1024,slope=1):
    #convert shape
    #scan = np.rollaxis(scan,2)
    
    #convert intensity scale
    vol_norm  = get_pixels_hu(scan,slope,intercept)
    
    #segmentation
    lung_mask = segment_lung_from_ct_scan(vol_norm)
    
    
    
    return lung_mask
